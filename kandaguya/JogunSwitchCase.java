/**
 * switch case문
 */
package kamdagiya;

public class JogunSwitchCase {
	public static void main(String[] args) {
		String name = "Rin";
		switch(name){
			case "Rin":
				System.out.println("Name is Rin");
			break;
			case "Jera":
				System.out.println("Name is Jera");
			break;
			default:
				System.out.println("나머지");
		}
	}
}
